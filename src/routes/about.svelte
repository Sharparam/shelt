<h1>About me</h1>

<p>
  I&rsquo;ve been programming for a large part of my life. Since I was about 10
  or 11 years old in fact. It started with animated PowerPoint slides,
  progressing into batch scripts on Windows, then some adventures in C++ before
  I landed in C&#9839; and Lua as my main languages.
</p>

<p>
  I don&rsquo;t remember how I ended up with C&#9839;, but since my platform at
  the time was Windows and I had started using Visual Studio for my C++ code,
  C&#9839; was close at hand. The reason for learning Lua was much simpler:
  World of Warcraft. Since the game uses Lua for its AddOn API, I started
  learning it when I wanted to add functionality to the game.
</p>

<p>
  My very first (at least first <em>real</em>) AddOn for WoW was
  <a href="https://www.curseforge.com/wow/addons/chocobo">Chocobo</a>. A friend
  wanted chocobo music from Final Fantasy to play when they used certain mounts
  in the game reminiscent of the chocobos, so I made it reality!
</p>

<p>
  On the C&#9839; side, I was doing mostly utility things and homework-style
  projects from the book <q>Head First C&#9839;</q>. Some of the projects can be
  found on my <a href="https://github.com/Sharparam">GitHub profile</a>, but
  others have sadly been lost, as I did not utilize any version control software
  in the very beginnings.
</p>

<p>
  I continued to maintain my programming interest throughout my early life and
  into higher education. Beginning with an engineering programme at high school
  and then pursuing a computer science degree at university.
</p>

<h2>Present day</h2>

<p>
  Today I work professionally as a programmer, living the dream as it were.
  Currently doing C&#9839; work at <a href="https://ninetech.com">Ninetech</a> as
  a Systems Developer. A job I managed to secure straight out of university, in large
  thanks to the mentoring programme at Karlstad University.
</p>

<p>
  C&#9839; is probably the language I&rsquo;m most passionate about, closely
  followed by Ruby. But I enjoy trying out various different languages and
  technologies!
</p>

